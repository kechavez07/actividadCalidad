#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

// Crear un PNG mínimo válido (1x1 transparente)
// Este es un PNG válido de 1x1 transparente que Tauri aceptará
const minimalPNG = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
  0x00, 0x00, 0x00, 0x0d, // IHDR chunk length
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4, 0x89,
  0x00, 0x00, 0x00, 0x0a, // IDAT chunk length
  0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0x00, 0x01, 0x00, 0x00, 0x05, 0x00, 0x01,
  0x0d, 0x0a, 0x2d, 0xb4,
  0x00, 0x00, 0x00, 0x00, // IEND chunk length
  0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82 // IEND
]);

const iconsDir = path.join(__dirname, 'src-tauri', 'icons');

// Crear carpeta si no existe
if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// Escribir iconos PNG (todos iguales para MVP)
const sizes = ['32x32.png', '128x128.png', '128x128@2x.png'];
sizes.forEach(size => {
  const filePath = path.join(iconsDir, size);
  fs.writeFileSync(filePath, minimalPNG);
  console.log(`✅ Created ${filePath}`);
});

// Crear icon.ico minimalista (formato BMP embebido en ICO)
// ICO header + BMP data para 1x1 transparente
const icoBuffer = Buffer.from([
  // ICO header
  0x00, 0x00, 0x01, 0x00, // Reserved + Type (1 = ICO)
  0x01, 0x00, // Number of images

  // Image directory entry
  0x01, 0x01, // Width, Height (1x1)
  0x00, 0x01, // Color count, Reserved
  0x01, 0x00, 0x20, 0x00, // Planes, Bits per pixel
  0x30, 0x00, 0x00, 0x00, // Size of image data
  0x16, 0x00, 0x00, 0x00, // Offset to image data

  // BMP image data (1x1, 32-bit ARGB)
  0x28, 0x00, 0x00, 0x00, // BMP header size
  0x01, 0x00, 0x00, 0x00, // Width
  0x02, 0x00, 0x00, 0x00, // Height (doubled for ICO)
  0x01, 0x00, 0x20, 0x00, // Planes, Bits per pixel
  0x00, 0x00, 0x00, 0x00, // Compression
  0x04, 0x00, 0x00, 0x00, // Image size
  0x00, 0x00, 0x00, 0x00, // H resolution
  0x00, 0x00, 0x00, 0x00, // V resolution
  0x00, 0x00, 0x00, 0x00, // Color palette size
  0x00, 0x00, 0x00, 0x00, // Important colors

  // Pixel data (1x1 blue pixel)
  0x00, 0x00, 0xff, 0xff,

  // AND mask (1x1)
  0x00, 0x00, 0x00, 0x00
]);

const icoPath = path.join(iconsDir, 'icon.ico');
fs.writeFileSync(icoPath, icoBuffer);
console.log(`✅ Created ${icoPath}`);

console.log('\n✅ All icons created successfully!');
